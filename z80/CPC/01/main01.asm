  
  ;------------------------------------------------------------
  ; Directives RASM
  ;------------------------------------------------------------
  
  RELEASE_DSK   = 1
  RELEASE_TAPE  = 1  

  ORG   #100
  RUN   #100
  
  ;------------------------------------------------------------
  ; Point d'entree principal - Adr #0100
  ;------------------------------------------------------------

  LD    A,#10         ; 2o 7c   1.75
  LD    B,#20         ; 2o 7c   1.75
  
  ADD   B             ; 1o 4c   1.00

  LD    (#110), A     ; 3o 13c  3.25
      
  ;------------------------------------------------------------
  ; Point de sortie principal - retour au basic
  ;------------------------------------------------------------
  
  RET                 ; 1o 10c  2.50
                      ; 9o 41c 10.25us (41/4)
        
  ;------------------------------------------------------------
  ; Directives RASM creation des fichiers
  ;------------------------------------------------------------
  
  IF RELEASE_DSK
        SAVE 'first.bin', #100, $-#100, DSK, 'first.dsk'
  ENDIF
  
  IF RELEASE_TAPE
        SAVE 'first.bin', #100, $-#100, TAPE,'first.cdt'
  ENDIF
  